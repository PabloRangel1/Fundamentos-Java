package ENUM;

public class Hokages {

    String nome;
    int idade;
    boolean vivoOuNao;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }

    public boolean isVivoOuNao() {
        return vivoOuNao;
    }

    public void setVivoOuNao(boolean vivoOuNao) {
        this.vivoOuNao = vivoOuNao;
    }
}
